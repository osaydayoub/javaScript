function countryInfo(country,population,capitalCity){
    return `${country} has ${population} million people and its capital city is ${capitalCity}`;
}
let spain=countryInfo('Spain',47,'Madrid');
let canada=countryInfo('Canada',39,'Ottawa')
let japan=countryInfo('Japan',125,'Tokyo')

console.log(spain);
console.log(canada);
console.log(japan);